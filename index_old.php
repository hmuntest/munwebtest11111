            <p>The Resolution Project, founded by Harvard WorldMUN alumni, is a global non-profit dedicated to fostering youth leadership development through collaborative social entrepreneurship for college students like you.  Each young leader attending WorldMUN has the capacity to begin changing the world today.  WorldMUN provides one of the best educational, networking, and skill-building experiences available to university students, and through our partnership with The Resolution Project, we also offer a pathway to action today.</p>
            <p>The Resolution Project inspires young leaders through their proven model of Resolution Social Venture Challenges (SVCs) and Resolution Fellowships:</p>
            <ul>
              <li>Resolution partners with existing youth summits like the one WorldMUN hosts; others include the Clinton Global Initiative University and the Youth Assembly at the United Nations.</li>
              <li>Resolution invites undergraduate students attending these conferences to propose social ventures through the SVC.  This is a multi-stage competition, combining web-based submissions, live rounds of presentations, and due diligence.</li>
              <li>Resolution then awards Resolution Fellowships to the most compelling leaders with the most promising proposals from the SVC.</li>
            </ul>
            <p>The Resolution Social Venture Challenge allows WorldMUN attendees to build teams and submit their plans for actual social ventures that will make a positive impact today.  Resolution will provide a grant of up to US $3,000 and strong advisory support for the winning Resolution Fellows and their ventures.  There are currently over 100 Resolution Fellows working on all 6 inhabited continents in diverse, high-impact fields such as water resources, sustainability, education, healthcare, and human rights. </p>
            <p><b>Register for the Resolution Social Venture Challenge</b></p>
